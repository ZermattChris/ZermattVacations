<h2> The Old House :: 4 Personen :: 4 Stern Ferienwohnung</h2>

<p>Die Wohnung ist ca. 100m&sup2;&nbsp; auf zwei
Stockwerken. Unten hat es
ein
Schwedenofen, die K&uuml;che mit einem Steamer und Mikrowelle, ein
Esstisch und ein Computer mit gratis WiFi. Ein Doppelschlafzimmer mit
zwei Einzelbetten, ein Badezimmer. Waschmaschine und Trockner stehen zu
Verf&uuml;gung. Im Obergeschoss ist das Wohnzimmer, durch die
grossen Fenster hat man eine fantastische Sicht aufs Matterhorn. Es hat
einen grossen TV mit Satelitenempfang und einen DVD. Ein
Doppelschlafzimmer mit einem en-suite Badezimmer. Da das Haus sehr alt
ist, sind die Badezimmer nur klein und die Verbindungstreppe sehr steil.</p>

