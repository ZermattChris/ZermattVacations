<p> A rare opportunity to enjoy a truly unique apartment,
situated in the heart of Zermatt on the old cobbled Hinterdorfstrasse.
The building
dates back to 1865 but was lovingly renovated by a local craftsman in
2007 combining elements of tradition &amp; contemporary styles.
Great care was taken during renovation to maximise space, add as much
light as possible and to incorporate the latest technology for modern
comfortable living.</p>

<span class="spacer"></span><!-- The Old House -->
<h2> The Old House :: 4 Persons :: 4 Star apartment</h2>

<p> Approx. 100m2 on 2 levels. Downstairs consists of an open
plan kitchen and dining room with a wood burning fire. The kitchen is
well equipped including steamer and microwave. The dining room desk is
equipped with a computer and free WiFi. A twin bedroom with bathroom
and a small utility room with a washer/dryer and space to store ski
equipment. Upstairs is a loft style living room with South facing
French windows offering a suberb Matterhorn view. A large Plasma TV
with UK satellite channels, DVD and SACD player. There is an option to
pipe UK radio stations to both bathrooms and downstairs living room. On
this level there is also a double bedroom with an en-suite bathroom.
Best use has been made of the available space on this listed property
so the bathrooms are small and the internal staircase is very steep. </p>

