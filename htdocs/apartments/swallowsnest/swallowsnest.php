<h2>Chalet Swallow's Nest :: 4 Persons :: 4 Star chalet</h2>

<p> This chalet is on 4 levels and offers 2 double bedrooms with
en-suite bathrooms. The lower level consists of 1 double bedroom with a
jacuzzi bathtub in the bedroom, wash basin and WC. The ground level
also has a double bedroom and en- suite bathroom with shower. On the
first floor is a fully fitted kitchen with a dining area and WC. On the
top level is a modern living room with an open fire, flat screen TV and
a music system that is wired to the other floors of the chalet. There
is free WiFi and a laptop is available upon request. </p>

