				<h2>Getting to Zermatt by train</h2>
				<p>
					The best airports to fly into are Zürich and Geneva. Both airports 
					are well organised and integrated into the Swiss rail network. 
					Tickets can be purchased upon arrival or for a cheaper option, a 
					Swiss Transfer Ticket can be pre-purchased online. 
					<a href="http://www.swisspasses.com/?pc=zermattvacations">www.swisspasses.com</a> 
					Train/rail times can be found at: <a href="http://www.sbb.ch/">www.sbb.ch</a>  
				</p>
				<p>
					Trains run approximately every hour and normally just one change of 
					trains is needed in Visp. Please be very careful of pick-pockets 
					especially at Geneva airport and also on the train. 
				</p>
				
				
				
