				<h2>Getting to Zermatt by road</h2>
				<p>
					If you are arriving by car, you will need to park your car in 
					Täsch which is a village 6km’s before Zermatt. No vehicles are 
					allowed to drive further without special authorisation. It is 
					recommended you park at a private parking, we recommend ‘Taxi Schaller’ 
					<a href="http://www.taxischaller.ch/">www.taxischaller.ch</a>  
					which you will find on the left of the main road. 
					In winter it is necessary to reserve. They will then transfer your 
					luggage into a taxi, drive you up to the edge of the village and 
					then will transfer your luggage into an electric taxi for your 
					transfer within Zermatt. You just need to tell them in which 
					house you are staying and they will know where to take you.
				</p>
				<p>
					Alternatively you can park at the main Matterhorn Terminal on 
					the right side of the main road.
					<a href="http://www.matterhornterminal.ch/">www.matterhornterminal.ch</a> 
					and then take the train up to Zermatt. Trains run every 20 minutes.
				</p>
				
